import random

ts = [
    (1,1),
    (2,3),
    (3,3),
    (1, 5),
    (1, 10),
    (10, 50),
    (10, 100),
    (100, 1000),
    (1000, 10000),
    (10000,100000),
    (10000,100000),
    (20000,100000),
    (30000,100000),
    (50000,100000),
    (70000,100000),
    (80000,100000),
    (90000,100000),
]

for t, (mn, mx) in enumerate(ts):

    with open('%02d.in' % t, 'w') as f:
        f.write('%d\n' % random.randint(mn, mx))

