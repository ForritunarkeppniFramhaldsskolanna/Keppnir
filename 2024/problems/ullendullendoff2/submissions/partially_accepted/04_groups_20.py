#!/usr/bin/python3
n = int(input())
names = [input() for _ in range(n)]
if n == 1:
    print(names[0])
elif n == 2:
    print(names[0])
    print(names[1])
elif n == 3:
    print(names[0])
    print(names[1])
    print(names[2])
elif n == 4:
    print(names[0])
    print(names[1])
    print(names[3])
    print(names[2])
